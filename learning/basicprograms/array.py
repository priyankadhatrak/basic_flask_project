num=[19,2,25,12,9,5,8,4]
#sort the array
num.sort()
print(num)

#count- counts how many times the parameter have arrived
a=num.count(2)
print(a)

#pop pop(index_value)
num.pop(3)
print(num)

#extend
num1=[11,22,33,44,55,77,23,10]
num.extend(num1)
print(num)

#reverse
num.reverse()
print(num)

#clear
num1.clear()
print(num1)

